class PilaApoyos:
    def __init__(self):
        self.items = []

    def esta_vacia(self):
        return len(self.items) == 0

    def apilar(self, item):
        self.items.append(item)

    def desapilar(self):
        if not self.esta_vacia():
            return self.items.pop()
        return None

    def mostrar(self):
        return list(reversed(self.items))  # Para mostrar de arriba hacia abajo
